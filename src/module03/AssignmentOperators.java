package src.module03;

public class AssignmentOperators {
    public static void main(String[] args) {
        
        int x = 10;

        x += 5;
        System.out.println(x); // 15

        x -= 5;
        System.out.println(x); // 10
        
        x *= 2;
        System.out.println(x); // 20
        
        x /= 2;
        System.out.println(x); // 10
    }
}
