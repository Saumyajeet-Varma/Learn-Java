package src.module03;

public class ComparisionOperators {
    public static void main(String[] args) {
        
        int x = 10;
        int y = 5;

        System.out.println(x == y); // false
        System.out.println(x != y); // true
        System.out.println(x > y); // true
        System.out.println(x < y); // false
        System.out.println(x >= y); // true
        System.out.println(x <= y); // false
    }
}
