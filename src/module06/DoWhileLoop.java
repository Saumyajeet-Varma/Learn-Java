package src.module06;

public class DoWhileLoop {
    public static void main(String[] args) {
        
        int i = 0;

        do {
            System.out.println(i);
            i++;
        } while(i < 5);




        // Beginning with false condition_________________________

        int x = 0;

        do {
            System.out.println(x);
            x--;
        } while(x > 0);
    }
}
