package src.module06;

public class WhileLoop {
    public static void main(String[] args) {
        
        int i = 0;

        while(i < 5) {
            System.out.println(i);
            i++;
        }




        // Beginning with false condition_________________________

        int x = 0;

        while(x > 0) {
            System.out.println(x);
            x--;
        }
    }
}
