package src.module02;

public class VarKeyword {
    public static void main(String[] args) {
        
        var x1 = 10;
        System.out.println(x1);
        
        var x2 = 'A';
        System.out.println(x2);

        var x3 = 1.99;
        System.out.println(x3);
        
        var x4 = "Hello";
        System.out.println(x4);

        var x5 = false;
        System.out.println(x5);
    }
}
