package src.module02;

public class DataTypes {
    public static void main(String[] args) {

        int x1 = 100000;
        System.out.println(x1 + " is Integer");

        char x2 = 'A';
        System.out.println(x2 + " is Character");

        float x3 = 1.01f;
        System.out.println(x3 + " is Float");
        
        double x4 = 1.01d;
        System.out.println(x4 + " is Double");
        
        boolean x5 = true;
        System.out.println(x5 + " is Boolean");
        
        String x6 = "Samm";
        System.out.println(x6 + " is String");
        
        byte x7 = 20;
        System.out.println(x7 + " is Byte");
        
        short x8 = 5000;
        System.out.println(x8 + " is Short");

        long x9 = 15000000000L;
        System.out.println(x9 + " is Long");
    }
}
